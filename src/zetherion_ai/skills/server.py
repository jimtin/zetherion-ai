    async def handle_worker_submit_job_result(self, request: web.Request) -> web.Response:
        if self._tenant_admin_manager is None:
            return web.json_response({"error": "Tenant admin is not configured"}, status=501)
        node_id = str(request.match_info["node_id"]).strip()
        job_id = str(request.match_info["job_id"]).strip()
        if not node_id or not job_id:
            return web.json_response({"error": "Missing node_id or job_id"}, status=400)

        try:
            raw_body = await request.text()
        except Exception:
            raw_body = ""
        payload = _extract_json_object(raw_body)
        if not payload:
            return web.json_response({"error": "Invalid JSON body"}, status=400)
        tenant_id = str(payload.get("tenant_id") or "").strip()
        if not tenant_id:
            return web.json_response({"error": "Missing tenant_id"}, status=400)

        try:
            session_ctx = await self._verify_worker_signature(
                request=request,
                tenant_id=tenant_id,
                node_id=node_id,
                raw_body=raw_body,
            )
            required_capabilities = self._parse_string_list(
                payload.get("required_capabilities"),
                "required_capabilities",
            )
            status_value = str(session_ctx.get("status") or "").strip().lower()
            health_value = str(session_ctx.get("health_status") or "").strip().lower()
            node_metadata_raw = session_ctx.get("node_metadata")
            node_metadata = node_metadata_raw if isinstance(node_metadata_raw, dict) else {}
            node_canary_enabled = self._tenant_admin_manager.is_worker_node_canary_enabled(
                tenant_id=tenant_id,
                node_id=node_id,
                metadata=node_metadata,
            )
            node_registered = status_value in {"registered", "active"}
            node_healthy = (
                status_value not in {"quarantined", "disabled"} and health_value == "healthy"
            )
            capability_allowlisted = await self._tenant_admin_manager.has_worker_capabilities(
                tenant_id=tenant_id,
                node_id=node_id,
                required_capabilities=required_capabilities,
            )
            decision = self._trust_policy_evaluator.evaluate(
                tenant_id=tenant_id,
                action="worker.job.complete",
                context={
                    "method": "POST",
                    "subpath": f"/worker/v1/nodes/{node_id}/jobs/{job_id}/result",
                    "node_registered": node_registered,
                    "node_healthy": node_healthy,
                    "capability_allowlisted": capability_allowlisted,
                    "node_canary_enabled": node_canary_enabled,
                },
            )
            if not decision.allowed:
                return web.json_response(
                    self._policy_error_payload(decision),
                    status=decision.status,
                )
            raw_output = payload.get("output")
            raw_error = payload.get("error")
            if raw_output is not None and not isinstance(raw_output, dict):
                raise ValueError("output must be an object when provided")
            if raw_error is not None and not isinstance(raw_error, dict):
                raise ValueError("error must be an object when provided")
            completion_status = str(payload.get("status") or "completed").strip().lower()
            submit_outcome = await self._tenant_admin_manager.submit_worker_job_result(
                tenant_id=tenant_id,
                node_id=node_id,
                job_id=job_id,
                completion_status=completion_status,
                output=raw_output if isinstance(raw_output, dict) else None,
                error=raw_error if isinstance(raw_error, dict) else None,
            )
            await self._tenant_admin_manager.record_worker_job_event(
                tenant_id=tenant_id,
                node_id=node_id,
                session_id=str(session_ctx.get("session_id") or ""),
                job_id=job_id,
                event_type=f"worker.job.result.{completion_status}",
                request_nonce=str(session_ctx.get("request_nonce") or ""),
                payload={
                    "status": completion_status,
                    "output": raw_output,
                    "error": raw_error,
                    "idempotent": bool(submit_outcome.get("idempotent")),
                },
            )
            await self._emit_worker_lifecycle_announcement(
                tenant_id=tenant_id,
                event_type="job_result",
                node_id=node_id,
                job_id=job_id,
                status=str(submit_outcome.get("status") or completion_status),
                metadata={"idempotent": bool(submit_outcome.get("idempotent"))},
            )
        except RuntimeError as exc:
            message = str(exc)
            if "replay" in message.lower():
                return web.json_response(
                    {
                        "ok": True,
                        "tenant_id": tenant_id,
                        "node_id": node_id,
                        "job_id": job_id,
                        "accepted": True,
                        "idempotent": True,
                    },
                    status=202,
                )
            return web.json_response({"error": str(exc)}, status=409)
        except ValueError as exc:
            return web.json_response({"error": str(exc)}, status=400)
        except Exception:
            log.exception(
                "worker_job_result_failed",
                tenant_id=tenant_id,
                node_id=node_id,
                job_id=job_id,
            )
            return web.json_response({"error": "Failed to accept worker result"}, status=502)

        return web.json_response(
            {
                "ok": True,
                "tenant_id": tenant_id,
                "node_id": node_id,
                "job_id": job_id,
                "accepted": True,
                "idempotent": bool(submit_outcome.get("idempotent")),
                "status": str(submit_outcome.get("status") or completion_status),
            },
            status=202,
        )

