    def _check_auth(self, request: web.Request) -> bool:
        """Check if request is authenticated.

        Args:
            request: The incoming request.

        Returns:
            True if authenticated or no secret required.
        """
        if not self._api_secret:
            return True

        provided = request.headers.get("X-API-Secret")
        return provided is not None and hmac.compare_digest(provided, self._api_secret)

    @staticmethod
    async def _read_json_object(request: web.Request) -> dict[str, Any]:
        """Read request JSON and ensure the payload is a JSON object."""
        try:
            data = await request.json()
        except (ValueError, JSONDecodeError) as err:
            raise ValueError("Invalid JSON body") from err
        if not isinstance(data, dict):
            raise ValueError("JSON body must be an object")
        return data

    @staticmethod
    def _parse_int(raw: Any, field_name: str) -> int:
        """Parse an integer field and raise ValueError on invalid input."""
        try:
            return int(raw)
        except (TypeError, ValueError) as err:
            raise ValueError(f"Invalid integer for '{field_name}'") from err

    @staticmethod
    def _parse_bool(raw: Any, field_name: str, default: bool = True) -> bool:
        """Parse a boolean field and raise ValueError on invalid input."""
        if raw is None:
            return default
        if isinstance(raw, bool):
            return raw
        if isinstance(raw, int | float):
            return bool(raw)
        if isinstance(raw, str):
            normalized = raw.strip().lower()
            if normalized in {"1", "true", "yes", "on"}:
                return True
            if normalized in {"0", "false", "no", "off"}:
                return False
        raise ValueError(f"Invalid boolean for '{field_name}'")

    @staticmethod
    def _parse_string_list(raw: Any, field_name: str) -> list[str]:
        """Parse a list of strings and strip empty entries."""
        if raw is None:
            return []
        if not isinstance(raw, list):
            raise ValueError(f"{field_name} must be an array of strings")
        output: list[str] = []
        for value in raw:
            if not isinstance(value, str):
                raise ValueError(f"{field_name} must be an array of strings")
            normalized = value.strip()
            if normalized:
                output.append(normalized)
        return output

